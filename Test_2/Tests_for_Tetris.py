class TestScore(unittest.TestCase):

    # Test change_score(self, points)
    # Score starts from 0
    def test_change_score(self):
        score = Score()
        points_def = score.score
        score.change_score(20)

        self.assertNotEqual(points_def, score.score)
        self.assertLess(points_def, score.score)
        self.assertEqual(score.score, 20)

        points_def = score.score
        score.change_score(20)

        self.assertNotEqual(points_def, score.score)
        self.assertLess(points_def, score.score)
        self.assertEqual(score.score, 40)


